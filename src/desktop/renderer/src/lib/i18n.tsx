const dictionaries = { zh, en } satisfies Record<DesktopLanguage, typeof zh>;

type TranslationKey = keyof typeof zh;

const I18nContext = createContext<{ language: DesktopLanguage; t: (key: TranslationKey, values?: Record<string, string | number>) => string }>({
	language: "zh",
	t: (key, values) => interpolate(zh[key], values),
});

function interpolate(text: string, values?: Record<string, string | number>): string {
	if (!values) {
		return text;
	}
	return text.replace(/\{(\w+)\}/g, (match, key) => String(values[key] ?? match));
}

